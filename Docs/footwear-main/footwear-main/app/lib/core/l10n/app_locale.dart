/// Supported app locales.
enum AppLocale {
  en(Locale('en'), 'English', TextDirection.ltr),
  ar(Locale('ar'), 'العربية', TextDirection.rtl),
  ur(Locale('ur'), 'اردو', TextDirection.rtl);

  final Locale locale;
  final String label;
  final TextDirection direction;
  const AppLocale(this.locale, this.label, this.direction);
}

/// Current locale state — defaults to English.
final appLocaleProvider = NotifierProvider<AppLocaleNotifier, AppLocale>(
  AppLocaleNotifier.new,
);

class AppLocaleNotifier extends Notifier<AppLocale> {
  @override
  AppLocale build() => AppLocale.en;

  void set(AppLocale locale) => state = locale;
}

/// Translation lookup shortcut.
String tr(String key, WidgetRef ref) {
  final locale = ref.watch(appLocaleProvider);
  return _translations[locale]?[key] ??
      _translations[AppLocale.en]?[key] ??
      key;
}

/// Standalone lookup (outside widgets — use with ref.read).
String trRead(String key, AppLocale locale) {
  return _translations[locale]?[key] ??
      _translations[AppLocale.en]?[key] ??
      key;
}

